import java.util.Scanner;

public class Palindrome {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        String palindrome = input.nextLine();
        int counter = 0;
        while (counter == palindrome.length() / 2) {
            palindrome.length();

            counter++;
        }
        input.close();
    }
}
